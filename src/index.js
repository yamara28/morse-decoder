const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

const NUMBER_TABLE = {
    11: '-',
    10: '.',
    "00": '',
};

function decode(expr) {

    const array = expr.match(/.{1,10}/g)
        .map(el => {
            return el.match(/.{1,2}/g)
        })
        .map(arr => {
            return arr.map(el => NUMBER_TABLE[el]).join('')
        })
        .map(el => MORSE_TABLE[el] || ' ').join('')

    return array
}

module.exports = {
    decode
}